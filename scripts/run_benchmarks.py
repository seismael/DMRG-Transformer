"""Proof-of-concept benchmark sweep for the Python reference.

Runs the three-way runoff (Adam / Dense exact / TT-DMRG) from
``docs/BENCHMARK.md`` at multiple tractable scales and writes a Markdown
results table to ``bench/RESULTS.md``.

The full 1024x1024 scale from BENCHMARK.md targets the Phase IV CUDA
microkernel; the pure-Python Jacobian materialization is O(batch * r^2 * p^2)
and does not fit in RAM at that size. We sweep smaller sizes that do.
"""
from __future__ import annotations

import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

from dmrg_transformer.bench import OptimizationBenchmark  # noqa: E402


CONFIGS = [
    # (in, out, batch, rank, adam_iters, sweeps)
    (64, 64, 512, 8, 500, 3),
    (100, 100, 512, 8, 500, 2),
    (144, 144, 256, 8, 500, 2),
]


def main() -> None:
    out_dir = ROOT / "bench"
    out_dir.mkdir(exist_ok=True)
    out_path = out_dir / "RESULTS.md"

    lines: list[str] = []
    lines.append("# DMRG-Transformer — Proof-of-Concept Benchmark Results\n")
    lines.append(
        "Generated by `scripts/run_benchmarks.py`. All runs on CPU (PyTorch "
        "float64). DMRG uses bidirectional sweeps; Adam is the reference "
        "baseline from [BENCHMARK.md](../docs/BENCHMARK.md); Dense Exact is "
        "`np.linalg.pinv`.\n"
    )
    lines.append(
        "> The full `1024x1024 / batch=2048` run in BENCHMARK.md targets the "
        "Phase IV CUDA microkernel (cuSOLVER + streamed contractions). The "
        "Python reference materializes the full local Jacobian and therefore "
        "caps out at moderate scales on CPU. Parity and correctness are "
        "proven by [Gate 3](../tests/test_gate3_exact_parity.py).\n"
    )
    lines.append(
        "| Layer | Batch | Rank | Compression | Optimizer | Final MSE | Time (s) | Params |\n"
        "| :---- | ----: | ---: | ----------: | :-------- | --------: | -------: | -----: |\n"
    )

    for in_f, out_f, batch, rank, adam_iters, sweeps in CONFIGS:
        label = f"{in_f}x{out_f}"
        t0 = time.perf_counter()
        bench = OptimizationBenchmark(
            in_features=in_f, out_features=out_f, batch_size=batch, rank=rank,
        )
        adam = bench.run_adam(iterations=adam_iters)
        dense = bench.run_dense_exact()
        dmrg = bench.run_dmrg(num_sweeps=sweeps)
        elapsed = time.perf_counter() - t0
        compression = bench.total_dense_params / bench.total_tt_params

        for res in (adam, dense, dmrg):
            lines.append(
                f"| {label} | {batch} | {rank} | {compression:.1f}x | "
                f"{res.name} | {res.mse:.4e} | {res.time_sec:.3f} | "
                f"{res.parameters:,} |\n"
            )
        print(f"[{label}] total wall: {elapsed:.1f}s")

    lines.append(
        "\n## Interpretation\n\n"
        "* **Adam** operates on a dense `(in, out)` matrix with a tuned "
        "learning rate; its MSE depends on step count and hyperparameters.\n"
        "* **Dense Exact** is the absolute $\\mathcal{O}(N^3)$ minimum for an "
        "unconstrained linear map — the lower bound any method can reach.\n"
        "* **TT-DMRG** is rank-constrained (compressed) but exact *within its "
        "manifold*. On targets that actually live at rank `<= r` it matches "
        "Dense exactly (see Gate 3); on the BENCHMARK.md synthetic "
        "`sin(X W) + noise` target the rank-r trade-off is visible.\n"
    )

    out_path.write_text("".join(lines), encoding="utf-8")
    print(f"\nWrote {out_path}")


if __name__ == "__main__":
    main()
