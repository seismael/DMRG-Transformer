"""Proof-of-concept benchmark sweep (GPU-only)."""
from __future__ import annotations

import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

from dmrg_transformer.bench import OptimizationBenchmark  # noqa: E402
from dmrg_transformer.core.device import describe_device, require_cuda  # noqa: E402

CONFIGS = [
    # (in, out, batch, rank, adam_iters, sweeps)
    # 1024x1024 is the BENCHMARK.md headline — runnable since the matrix-free
    # block-diagonal solver landed (see docs/COMPLIANCE.md Phase A1). For
    # rigorous mean±std + memory + FLOPs at the headline scale, use
    # `python scripts/run_headline_benchmark.py` (writes bench/HEADLINE.md).
    (64,   64,   512,  8,  500, 3),
    (144,  144,  512,  8,  500, 2),
    (256,  256,  1024, 8,  500, 2),
    (1024, 1024, 2048, 32, 500, 2),
]


def main() -> None:
    device = require_cuda()
    out_dir = ROOT / "bench"
    out_dir.mkdir(exist_ok=True)
    out_path = out_dir / "RESULTS.md"

    lines: list[str] = []
    lines.append("# DMRG-Transformer — Benchmark Results (GPU)\n\n")
    lines.append(f"**Device:** `{describe_device()}`\n\n")
    lines.append(
        "Generated by `scripts/run_benchmarks.py`. All runs on CUDA (PyTorch "
        "float64). DMRG uses bidirectional sweeps; Adam is the reference "
        "baseline from [BENCHMARK.md](../docs/BENCHMARK.md); Dense Exact is "
        "`torch.linalg.lstsq` via cuSOLVER. Wall-time includes "
        "`torch.cuda.synchronize()`.\n\n"
    )
    lines.append(
        "| Layer | Batch | Rank | Compression | Optimizer | Final MSE | Time (s) | Params |\n"
        "| :---- | ----: | ---: | ----------: | :-------- | --------: | -------: | -----: |\n"
    )

    for in_f, out_f, batch, rank, adam_iters, sweeps in CONFIGS:
        label = f"{in_f}x{out_f}"
        t0 = time.perf_counter()
        bench = OptimizationBenchmark(
            in_features=in_f, out_features=out_f, batch_size=batch, rank=rank,
            device=device,
        )
        adam = bench.run_adam(iterations=adam_iters)
        dense = bench.run_dense_exact()
        dmrg = bench.run_dmrg(num_sweeps=sweeps)
        elapsed = time.perf_counter() - t0
        compression = bench.total_dense_params / bench.total_tt_params

        for res in (adam, dense, dmrg):
            lines.append(
                f"| {label} | {batch} | {rank} | {compression:.1f}x | "
                f"{res.name} | {res.mse:.4e} | {res.time_sec:.3f} | "
                f"{res.parameters:,} |\n"
            )
        print(f"[{label}] total wall: {elapsed:.1f}s")

    lines.append(
        "\n## Interpretation\n\n"
        "* **Adam** operates on a dense `(in, out)` matrix with a tuned "
        "learning rate; its MSE depends on step count and hyperparameters.\n"
        "* **Dense Exact** is the absolute $\\mathcal{O}(N^3)$ minimum for an "
        "unconstrained linear regression.\n"
        "* **TT-DMRG** replaces the dense weight with a rank-r Tensor-Train "
        "and converges to the rank-constrained optimum in a bounded number "
        "of bidirectional sweeps — no learning rate, no iteration budget.\n"
    )

    out_path.write_text("".join(lines), encoding="utf-8")
    print(f"wrote: {out_path}")


if __name__ == "__main__":
    main()
